    /**
     * Handle the User "deleted" event.
     *
     * @param  \App\Models\User  $user
     * @return void
     */
    public function deleted(User $user)
    {
        $company = $user->company;
        if ($user->id == $company->owner_id){
            SocialAccount::where('company_id', $company->id)->each(function ($account){
                $service = SocialAccountService::factory($account);
                $service->delete();
            });
            $company->forceDelete();
        }
    }

